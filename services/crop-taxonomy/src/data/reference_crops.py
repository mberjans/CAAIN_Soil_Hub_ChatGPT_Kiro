def build_reference_crops_dataset() -> List[ComprehensiveCropData]:
    """Instantiate reference crops as ComprehensiveCropData objects."""
    crops: List[ComprehensiveCropData] = []
    for raw_data in REFERENCE_CROP_DATA:
        prepared_data: Dict[str, Any] = {}
        for key, value in raw_data.items():
            prepared_data[key] = value
        if "data_source" not in prepared_data:
            prepared_data["data_source"] = "reference_dataset"
        if "confidence_score" not in prepared_data:
            prepared_data["confidence_score"] = 0.9
        timestamp = datetime.utcnow()
        if "updated_at" not in prepared_data:
            prepared_data["updated_at"] = timestamp
        if "last_updated" not in prepared_data:
            prepared_data["last_updated"] = prepared_data["updated_at"]
        crop = ComprehensiveCropData(**prepared_data)
        crops.append(crop)
    return crops
